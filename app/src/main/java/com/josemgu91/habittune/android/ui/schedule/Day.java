package com.josemgu91.habittune.android.ui.schedule;

import java.util.Calendar;

public enum Day {

    MONDAY(0),
    TUESDAY(1),
    WEDNESDAY(2),
    THURSDAY(3),
    FRIDAY(4),
    SATURDAY(5),
    SUNDAY(6);

    private final int dayValue;

    Day(final int dayValue) {
        this.dayValue = dayValue;
    }

    public Day fromJavaCalendar(final int dayOfTheWeek) {
        switch (dayOfTheWeek) {
            case Calendar.MONDAY:
                return MONDAY;
            case Calendar.TUESDAY:
                return TUESDAY;
            case Calendar.WEDNESDAY:
                return WEDNESDAY;
            case Calendar.THURSDAY:
                return THURSDAY;
            case Calendar.FRIDAY:
                return FRIDAY;
            case Calendar.SATURDAY:
                return SATURDAY;
            case Calendar.SUNDAY:
                return SUNDAY;
            default:
                throw new RuntimeException(
                        String.format("Unknown dayOfTheWeek (%s) value!", String.valueOf(dayOfTheWeek))
                );
        }
    }
}
